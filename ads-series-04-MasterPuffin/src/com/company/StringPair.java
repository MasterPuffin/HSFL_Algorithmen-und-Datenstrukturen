package com.company;

public class StringPair {
    private String stringA;
    private String stringB;

    public StringPair(String stringA, String stringB) {
        this.stringA = stringA;
        this.stringB = stringB;
    }

    public String getStrA() {
        return this.stringA;
    }

    public String getStrB() {
        return this.stringB;
    }
}
